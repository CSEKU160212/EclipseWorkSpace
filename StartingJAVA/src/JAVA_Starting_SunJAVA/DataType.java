package JAVA_Starting_SunJAVA;

public class DataType {

	static boolean booleanVariable = true;
	static char charVariable = 'M';
	static byte byteVariable = 100;
	static short shortVariable = 5000;
	static int intVariable = -3200000;
	static long aLongVariable = 343489241;
	static float floatVariable = 45.90f;
	static double doubleVariable = 32987.21;
	
	public static void main(String [] args)
	{
		
		System.out.println(booleanVariable);
		System.out.println(charVariable);
		System.out.println(byteVariable);
		System.out.println(shortVariable);
		System.out.println(intVariable);
		System.out.println(aLongVariable);
		System.out.println(floatVariable);
		System.out.println(doubleVariable);
		
	}
	
	
}
