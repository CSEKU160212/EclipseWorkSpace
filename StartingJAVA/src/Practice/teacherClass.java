package Practice;

public class teacherClass extends HumanClass{

	public String teacherName;
	public String designation;
	public String institutions;
	public String nameOfSubject;
	
	public teacherClass(String Name, String designation, String institute, String subject)
	{
			super();
			this.teacherName=Name;
			this.designation=designation;
			this.institutions=institute;
			this.nameOfSubject=subject;
			
	}
	
	
}
