package Practice;

public class HumanClass {

	private int noOfLeg;
	private int noOfEye;
	private int noOfNose;
	private int noOfHand;
	private String humanitiesYesOrNo;
	
	public HumanClass() 
	{
		noOfLeg=2;
		noOfEye=2;
		noOfNose=1;
		noOfHand=2;
		humanitiesYesOrNo="Yes";
	}
	
	
	public int getNoOfLeg() {
		return noOfLeg;
	}
	public void setNoOfLeg(int noOfLeg) {
		this.noOfLeg = noOfLeg;
	}
	public int getNoOfEye() {
		return noOfEye;
	}
	public void setNoOfEye(int noOfEye) {
		this.noOfEye = noOfEye;
	}
	public int getNoOfNose() {
		return noOfNose;
	}
	public void setNoOfNose(int noOfNose) {
		this.noOfNose = noOfNose;
	}
	public int getNoOfHand() {
		return noOfHand;
	}
	public void setNoOfHand(int noOfHand) {
		this.noOfHand = noOfHand;
	}
	public String getHumanitiesYesOrNo() {
		return humanitiesYesOrNo;
	}
	public void setHumanitiesYesOrNo(String humanitiesYesOrNo) {
		this.humanitiesYesOrNo = humanitiesYesOrNo;
	}
	
	
	
	
}
